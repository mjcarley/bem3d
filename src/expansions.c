gint htri_select_expansion(gdouble dx, gdouble tol,
			   gdouble **coefficients_cos, gint *nc,
			   gdouble **coefficients_sin, gint *ns)

{
  gint i, j ;

  g_assert(dx < 1.58) ;

  if ( dx == 0.0 ) {
    *coefficients_cos = _coefficients_cos_T0 ;
    *coefficients_sin = _coefficients_sin_T0 ;
    *nc = _n_coefficients_cos_T0 ;
    *ns = _n_coefficients_sin_T0 ;

    return 0 ;
  }

  /*find tol and interval in index system*/
  i = j = 0 ; 
  while ( i < _coefficients_ntol && _coefficients_tol[i] > tol ) i ++ ;
  while ( j < _coefficients_ndx && _coefficients_dx[j] < dx ) j ++ ;

  g_assert(dx  <= _coefficients_dx[j]) ;
  g_assert(tol >= _coefficients_tol[i]) ;

  i = i*_coefficients_ndx + j ;

  *coefficients_sin = _coefficients[2*i+1] ;
  *coefficients_cos = _coefficients[2*i+0] ;
  *ns = _n_coefficients[2*i+1] ;
  *nc = _n_coefficients[2*i+0] ;

  return 0 ;
}

